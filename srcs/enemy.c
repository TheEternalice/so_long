#include "../include/so_long.h"

void	enemy_move(t_struct *stru, int x, int y)
{
	if (stru->map[y][x + 1] == '0' || stru->map[y][x + 1] == 'P')
	{
		stru->map[y][x] = '0';
		stru->map[y][x + 1] = 'S';
	}
}
